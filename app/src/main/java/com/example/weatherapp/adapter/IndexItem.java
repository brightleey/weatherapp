public class IndexItem {
    private String txt;
    private int imageId;
    private String desc;

    public void setTxt(String txt) {
        this.txt = txt;
    }

    public void setImageId(int imageId) {
        this.imageId = imageId;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getTxt() {
        return txt;
    }

    public int getImageId() {
        return imageId;
    }

    public String getDesc() {
        return desc;
    }
}
